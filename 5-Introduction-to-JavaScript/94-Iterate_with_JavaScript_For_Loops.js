/* for loops are the most common loop for iteration. It accepts three optional
expression which have to be separated by semi-colon. for (intialization;
condition; final-expression)*/
// Example
var ourArray = [];

for (var i = 0; i < 5; i++) {
  ourArray.push(i);
}

